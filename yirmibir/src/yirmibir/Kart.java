package yirmibir;

public class Kart {
	private String isim;
	private int deger;
	private String renkli;
	public String getIsim() {
		return isim;
	}
	public void setIsim(String isim) {
		this.isim = isim;
	}
	public int getDeger() {
		return deger;
	}
	public void setDeger(int deger) {
		this.deger = deger;
	}
	public String getRenkli() {
		return renkli;
	}
	public void setRenkli(String renkli) {
		this.renkli = renkli;
	}
	
	

}
